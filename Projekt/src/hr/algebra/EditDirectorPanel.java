    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (formValid()) {

            if (listMovies.getSelectedValues().length != 0) {
                try {
                    Person person = new Person(tfFirstName.getText().trim(), tfLastName.getText().trim());
                    Arrays.asList(listMovies.getSelectedValues()).forEach(m -> {
                        try {
                            personRepository.create(((Movie) m).getId(), person);
                        } catch (Exception ex) {
                            Logger.getLogger(EditActorsPanel.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    });
                    personTableModel.setPersonos(personRepository.selectDirectors());
                    clearForm();
                } catch (Exception e) {
                }
            } else {
                MessageUtils.showInformationMessage("Information", "You need select at least one movie to add director");
            }

        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedPerson == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose person to update");
            return;
        }
        if (formValid()) {
            try {
                selectedPerson.setFirstName(tfFirstName.getText().trim());
                selectedPerson.setLastName(tfLastName.getText().trim());
                personRepository.update(selectedPerson.getId(), selectedPerson);
                personTableModel.setPersonos(personRepository.selectDirectors());
                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(EditActorsPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedPerson == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose person to update");
            return;
        }
        if (listMovies.getSelectedValues().length == 0) {
            MessageUtils.showInformationMessage("Information", "You need to select at least one movie to delete director.");
            return;
        }
        if (MessageUtils.showConfirmDialog(
                "Delete director",
                "Do you really want to delete director for this movie?") == JOptionPane.YES_OPTION) {
            try {
                System.out.println(Arrays.asList(listMovies.getSelectedValues()).size());
                Arrays.asList(listMovies.getSelectedValues()).forEach(movie -> {
                    try {
                        personRepository.delete(((Movie) movie).getId(), selectedPerson.getId(), 2);
                    } catch (Exception ex) {
                        Logger.getLogger(EditActorsPanel.class.getName()).log(Level.SEVERE, null, ex);
                    }
                });

                personTableModel.setPersonos(personRepository.selectDirectors());
                clearForm();
            } catch (Exception ex) {
                Logger.getLogger(EditActorsPanel.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete director!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

