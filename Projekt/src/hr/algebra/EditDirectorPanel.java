    private void init() {
        try {
            initValidation();
            initRepostiry();
            initTable();
            loadMovieModel();
        } catch (Exception ex) {
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            Logger.getLogger(EditDirectorPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void initValidation() {
        validationFields = Arrays.asList(tfFirstName, tfLastName);
        errorLabels = Arrays.asList(lbErrorFirstName, lbErrorLastName);
    }

    private void initRepostiry() throws Exception {
        personRepository = RepositoryFactory.<PersonRepositry>getRepository();
        repository = RepositoryFactory.<MovieRepository>getRepository();
    }

    private void initTable() throws Exception {
        tbDirectors.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbDirectors.setAutoCreateRowSorter(true);
        tbDirectors.setRowHeight(25);
        new Thread(() -> {
            try {
                personTableModel = new PersonTableModel(personRepository.selectDirectors());
            } catch (Exception ex) {
                Logger.getLogger(EditDirectorPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            EventQueue.invokeLater(() -> {
                tbDirectors.setModel(personTableModel);
            });
        }).start();
    }

    private void loadMovieModel() throws Exception {
        new Thread(() -> {
            if (lock.readLock().tryLock()) {
                try {
                    moviesModel.clear();
                    repository.select().forEach(moviesModel::addElement);
                    listMovies.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
                    listMovies.setSelectedIndex(0);
                    EventQueue.invokeLater(() -> {
                        System.out.println("aaaaa");
                        listMovies.setModel(moviesModel);
                    });

                } catch (Exception ex) {
                    Logger.getLogger(EditDirectorPanel.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    lock.readLock().unlock();
                }
            }
        }).start();
    }

    private boolean formValid() {
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setText(validationFields.get(i).getText().trim().isEmpty() ? "X" : "");
        }

        return ok;
    }

    private void clearForm() throws Exception {
        validationFields.forEach(e -> e.setText(""));
        errorLabels.forEach(e -> e.setText(""));
        selectedPerson = null;
        loadMovieModel();
    }

    private void showDirector() {
        try {
            int selectedRow = tbDirectors.getSelectedRow();
            int rowIndex = tbDirectors.convertRowIndexToModel(selectedRow);
            int selectedActorId = (int) personTableModel.getValueAt(rowIndex, 0);
            Optional<Person> optDirector = personRepository.selecActor(selectedActorId);
            if (optDirector.isPresent()) {
                selectedPerson = optDirector.get();
                fillForm(selectedPerson);

            }
        } catch (Exception ex) {
            Logger.getLogger(EditActorsPanel.class
                    .getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to show director!");
        }
    }
    public ReadWriteLock lock = new ReentrantReadWriteLock();

    private void fillForm(Person selectedPerson) throws Exception {
        tfFirstName.setText(selectedPerson.getFirstName());
        tfLastName.setText(selectedPerson.getPasswod());
        new Thread(() -> {
            try {
                lock.writeLock().lock();
                moviesModel.clear();
                repository.select(selectedPerson, 2).forEach(moviesModel::addElement);
                EventQueue.invokeLater(() -> {
                    listMovies.setModel(moviesModel);
                });

            } catch (Exception ex) {
                Logger.getLogger(EditDirectorPanel.class
                        .getName()).log(Level.SEVERE, null, ex);
            } finally {
                lock.writeLock().unlock();
            }
        }).start();
    }
